    {
        $data = [];
        $items = Item::all();
        foreach ($items as $item)
        {
            $data[] = [
                'item_id' => $item->item_id,
                'name' => $item->name,
                'price' => $item->base_price,
                'thumbnail' => $item->post->image_path
            ];
        }
        return $data;
    }

